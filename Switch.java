public class Switch {
    public static void main(String[] args) {
        String var ="Shubham";
// Inhance Switch

        switch (var){
            case "Shubham" -> {
            System.out.println("Hii");
            }
            case "Sanket"->
            System.out.println("You are going to join Job!");
            
            case "Vishwa"->
            System.out.println("You are going to get Retired !");
            
            default-> System.out.println("Enjoy Your Life");
        }
    }
   
}
